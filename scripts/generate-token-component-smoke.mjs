import { existsSync, readdirSync, statSync, writeFileSync } from "node:fs";
import { join } from "node:path";

function listComponentDirs() {
  const root = "packages/react/src/components";

  if (!existsSync(root)) return [];

  return readdirSync(root)
    .filter((entry) => {
      const fullPath = join(root, entry);
      return statSync(fullPath).isDirectory();
    })
    .filter((entry) => !entry.startsWith("."))
    .sort((a, b) => a.localeCompare(b));
}

function tokenExportName(componentName) {
  return `nc${componentName}TokenNames`;
}

const componentNames = listComponentDirs();
const tokenNames = componentNames.map(tokenExportName);

const imports = tokenNames.length > 0
  ? `import {\n${tokenNames.map((name) => `  ${name},`).join("\n")}\n} from "../index";`
  : "";

const registryEntries = componentNames.map((componentName) => {
  const exportName = tokenExportName(componentName);

  return `  ["${componentName}", ${exportName}],`;
}).join("\n");

const content = [
  "/* eslint-disable */",
  "// This file is generated by scripts/generate-token-component-smoke.mjs.",
  "// It intentionally imports every component token list from the token package barrel.",
  "// TypeScript build must fail if a component token file is not publicly exported.",
  "",
  imports,
  "",
  "export const noctraTokenComponentSmokeRegistry = [",
  registryEntries,
  "] as const;",
  "",
  "export const noctraTokenComponentSmokeNames = noctraTokenComponentSmokeRegistry.map(([name]) => name);",
  "",
  "export const noctraTokenComponentSmokeTokenCount = noctraTokenComponentSmokeRegistry.reduce(",
  "  (total, [, tokens]) => total + tokens.length,",
  "  0",
  ");",
  "",
  `export const noctraTokenComponentSmokeSummary = ${JSON.stringify({
    generatedAt: new Date().toISOString(),
    componentCount: componentNames.length
  }, null, 2)} as const;`,
  ""
].join("\n");

writeFileSync("packages/tokens/src/generated/component-token-smoke.generated.ts", content, "utf8");

const report = [
  "# Noctra Token Component Smoke Export Report",
  "",
  `Generated: ${new Date().toISOString()}`,
  "",
  `Components imported: ${componentNames.length}`,
  "",
  "## Token Exports",
  "",
  ...componentNames.map((name) => `- ${tokenExportName(name)}`)
].join("\n");

writeFileSync("token-component-smoke-export-report.md", `${report}\n`, "utf8");

console.log(`Token component smoke file generated. Components: ${componentNames.length}. Report: token-component-smoke-export-report.md`);