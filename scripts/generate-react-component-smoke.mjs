import { existsSync, readdirSync, statSync, writeFileSync } from "node:fs";
import { join } from "node:path";

function listComponentDirs() {
  const root = "packages/react/src/components";

  if (!existsSync(root)) return [];

  return readdirSync(root)
    .filter((entry) => {
      const fullPath = join(root, entry);
      return statSync(fullPath).isDirectory();
    })
    .filter((entry) => !entry.startsWith("."))
    .sort((a, b) => a.localeCompare(b));
}

const componentNames = listComponentDirs();

const imports = componentNames.length > 0
  ? `import {\n${componentNames.map((name) => `  ${name},`).join("\n")}\n} from "../components";`
  : "";

const smokeEntries = componentNames.map((name) => `  ["${name}", ${name}],`).join("\n");

const content = [
  "/* eslint-disable */",
  "// This file is generated by scripts/generate-react-component-smoke.mjs.",
  "// It intentionally imports every component directory export from the React barrel.",
  "// TypeScript build must fail if a component directory is not publicly exported.",
  "",
  'import { createElement, Fragment } from "react";',
  'import type { ComponentType, ReactElement } from "react";',
  imports,
  "",
  "type SmokeComponent = ComponentType<Record<string, unknown>>;",
  "",
  "export const noctraReactComponentSmokeRegistry = [",
  smokeEntries,
  "] as const;",
  "",
  "export const noctraReactComponentSmokeNames = noctraReactComponentSmokeRegistry.map(([name]) => name);",
  "",
  "export function NoctraReactComponentSmokeRender(): ReactElement {",
  "  return createElement(",
  "    Fragment,",
  "    null,",
  "    ...noctraReactComponentSmokeRegistry.map(([name, Component]) =>",
  "      createElement(Component as SmokeComponent, { key: name, \"data-smoke-component\": name })",
  "    )",
  "  );",
  "}",
  "",
  `export const noctraReactComponentSmokeSummary = ${JSON.stringify({
    generatedAt: new Date().toISOString(),
    componentCount: componentNames.length
  }, null, 2)} as const;`,
  ""
].join("\n");

writeFileSync("packages/react/src/generated/component-smoke.generated.tsx", content, "utf8");

const report = [
  "# Noctra React Component Smoke Export Report",
  "",
  `Generated: ${new Date().toISOString()}`,
  "",
  `Components imported: ${componentNames.length}`,
  "",
  "## Components",
  "",
  ...componentNames.map((name) => `- ${name}`)
].join("\n");

writeFileSync("react-component-smoke-export-report.md", `${report}\n`, "utf8");

console.log(`React component smoke file generated. Components: ${componentNames.length}. Report: react-component-smoke-export-report.md`);