import { existsSync, readdirSync, readFileSync, statSync, writeFileSync } from "node:fs";
import { join } from "node:path";

function readText(path) {
  return existsSync(path) ? readFileSync(path, "utf8").replace(/^\uFEFF/, "") : "";
}

function toKebabCase(value) {
  return value
    .replace(/([a-z0-9])([A-Z])/g, "$1-$2")
    .replace(/([A-Z])([A-Z][a-z])/g, "$1-$2")
    .toLowerCase();
}

function listComponentDirs() {
  const root = "packages/react/src/components";

  if (!existsSync(root)) return [];

  return readdirSync(root)
    .filter((entry) => {
      const fullPath = join(root, entry);
      return statSync(fullPath).isDirectory();
    })
    .filter((entry) => !entry.startsWith("."))
    .sort((a, b) => a.localeCompare(b));
}

function packageExportExists(packageJsonPath, exportKey) {
  const raw = readText(packageJsonPath);
  if (!raw) return false;

  const json = JSON.parse(raw);
  return Boolean(json.exports?.[exportKey]);
}

const componentNames = listComponentDirs();
const componentsCss = readText("packages/styles/src/components.css");

const rows = componentNames.map((name) => {
  const kebab = toKebabCase(name);
  const cssFile = `packages/styles/src/components/${kebab}.css`;
  const importLine = `@import "./components/${kebab}.css";`;
  const exportKey = `./components/${kebab}.css`;
  const cssClass = `nc-${kebab}-root`;

  return {
    name,
    kebab,
    cssFile,
    cssClass,
    importLine,
    exportKey,
    hasCssFile: existsSync(cssFile),
    hasImport: componentsCss.includes(importLine),
    hasPackageExport: packageExportExists("packages/styles/package.json", exportKey),
    hasRootClass: readText(cssFile).includes(cssClass)
  };
});

const problems = [];

for (const row of rows) {
  if (!row.hasCssFile) problems.push(`${row.name}: missing CSS file ${row.cssFile}`);
  if (!row.hasImport) problems.push(`${row.name}: missing components.css import ${row.importLine}`);
  if (!row.hasPackageExport) problems.push(`${row.name}: missing styles package export ${row.exportKey}`);
  if (!row.hasRootClass) problems.push(`${row.name}: CSS file does not contain .${row.cssClass}`);
}

const generatedContent = [
  "/* eslint-disable */",
  "// This file is generated by scripts/generate-style-component-smoke.mjs.",
  "// It keeps a typed registry of every component CSS entry expected by the styles package.",
  "",
  "export type NoctraStyleComponentSmokeItem = {",
  "  name: string;",
  "  kebab: string;",
  "  cssFile: string;",
  "  cssClass: string;",
  "  importLine: string;",
  "  exportKey: string;",
  "};",
  "",
  "export const noctraStyleComponentSmokeRegistry = [",
  ...rows.map((row) => `  ${JSON.stringify({
    name: row.name,
    kebab: row.kebab,
    cssFile: row.cssFile,
    cssClass: row.cssClass,
    importLine: row.importLine,
    exportKey: row.exportKey
  })},`),
  "] as const satisfies readonly NoctraStyleComponentSmokeItem[];",
  "",
  "export const noctraStyleComponentSmokeNames = noctraStyleComponentSmokeRegistry.map((component) => component.name);",
  "export const noctraStyleComponentSmokeCssClasses = noctraStyleComponentSmokeRegistry.map((component) => component.cssClass);",
  "",
  `export const noctraStyleComponentSmokeSummary = ${JSON.stringify({
    generatedAt: new Date().toISOString(),
    componentCount: componentNames.length,
    problemCount: problems.length
  }, null, 2)} as const;`,
  ""
].join("\n");

writeFileSync("packages/styles/src/generated/component-style-smoke.generated.ts", generatedContent, "utf8");

const report = [
  "# Noctra Style Component Smoke Export Report",
  "",
  `Generated: ${new Date().toISOString()}`,
  "",
  `Components scanned: ${componentNames.length}`,
  `Problems found: ${problems.length}`,
  "",
  "## Style Matrix",
  "",
  "| Component | Kebab | CSS file | CSS import | Package export | Root class |",
  "|---|---|---|---|---|---|",
  ...rows.map((row) => {
    return `| ${row.name} | ${row.kebab} | ${row.hasCssFile ? "OK" : "MISSING"} | ${row.hasImport ? "OK" : "MISSING"} | ${row.hasPackageExport ? "OK" : "MISSING"} | ${row.hasRootClass ? "OK" : "MISSING"} |`;
  }),
  "",
  "## Problems",
  "",
  ...(problems.length > 0 ? problems.map((problem) => `- ${problem}`) : ["- None"])
].join("\n");

writeFileSync("style-component-smoke-export-report.md", `${report}\n`, "utf8");

console.log(`Style component smoke file generated. Components: ${componentNames.length}. Problems: ${problems.length}. Report: style-component-smoke-export-report.md`);