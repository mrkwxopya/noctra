import { existsSync, readdirSync, statSync, writeFileSync } from "node:fs";
import { join } from "node:path";

function toKebabCase(value) {
  return value
    .replace(/([a-z0-9])([A-Z])/g, "$1-$2")
    .replace(/([A-Z])([A-Z][a-z])/g, "$1-$2")
    .toLowerCase();
}

function listComponentDirs() {
  const root = "packages/react/src/components";

  if (!existsSync(root)) return [];

  return readdirSync(root)
    .filter((entry) => {
      const fullPath = join(root, entry);
      return statSync(fullPath).isDirectory();
    })
    .filter((entry) => !entry.startsWith("."))
    .sort((a, b) => a.localeCompare(b));
}

const componentNames = listComponentDirs();

const rows = componentNames.map((name) => ({
  name,
  kebab: toKebabCase(name),
  cssClass: `nc-${toKebabCase(name)}-root`,
  importPath: `@noctra/react/${toKebabCase(name)}`,
  status: "registered"
}));

const content = [
  "/* eslint-disable */",
  "// This file is generated by scripts/generate-docs-component-registry.mjs.",
  "// It keeps the docs source aware of every public Noctra component without importing runtime code.",
  "",
  "export type NoctraDocsComponentRegistryItem = {",
  "  name: string;",
  "  kebab: string;",
  "  cssClass: string;",
  "  importPath: string;",
  "  status: string;",
  "};",
  "",
  "export const noctraDocsComponentRegistry = [",
  ...rows.map((row) => `  ${JSON.stringify(row)},`),
  "] as const satisfies readonly NoctraDocsComponentRegistryItem[];",
  "",
  "export const noctraDocsComponentNames = noctraDocsComponentRegistry.map((component) => component.name);",
  "export const noctraDocsComponentKebabNames = noctraDocsComponentRegistry.map((component) => component.kebab);",
  "",
  `export const noctraDocsComponentRegistrySummary = ${JSON.stringify({
    generatedAt: new Date().toISOString(),
    componentCount: componentNames.length
  }, null, 2)} as const;`,
  ""
].join("\n");

writeFileSync("apps/docs/src/generated/noctra-component-registry.generated.ts", content, "utf8");

const report = [
  "# Noctra Docs Component Registry Auto-Heal Report",
  "",
  `Generated: ${new Date().toISOString()}`,
  "",
  `Components registered: ${componentNames.length}`,
  "",
  "## Registered Components",
  "",
  ...rows.map((row) => `- ${row.name} (${row.kebab})`)
].join("\n");

writeFileSync("docs-component-registry-auto-heal-report.md", `${report}\n`, "utf8");

console.log(`Docs component registry generated. Components: ${componentNames.length}. Report: docs-component-registry-auto-heal-report.md`);